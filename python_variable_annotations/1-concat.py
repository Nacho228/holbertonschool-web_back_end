#!/usr/bin/env python3
""" type-annotated function concat that takes a
string str1 and a string str2 as arguments"""


def concat(str1: str, str2: str) -> str:
    """ Concatenates two strings.
    Params:
    str1: first string.
    str2: second string.
    Return: String.
    """
    return (str1 + str2)
